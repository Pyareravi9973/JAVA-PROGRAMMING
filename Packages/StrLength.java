// Enter any string and find its length 
import java.util.*;
class StrLength 
{
    public static void main(String[] args)
    {
     String s1=" ";
     int l;
     Scanner sc=new Scanner(System.in);
     System.out.println("Enter any String:");
     s1=sc.next();
     l=s1.length();
     System.out.println("length of String:"+l);     
    }
}